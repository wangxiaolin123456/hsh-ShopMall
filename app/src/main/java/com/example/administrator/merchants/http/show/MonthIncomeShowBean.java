public class MonthIncomeShowBean {
    private String time;//时间
    private String begintime;//开始日期
    private double dealAmount;//成交总额
    private double getProfit;//获得收益

    public void setBegintime(String begintime) {
        this.begintime = begintime;
    }

    public String getBegintime() {
        return begintime;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
    public double getDealAmount() {
        return dealAmount;
    }

    public void setDealAmount(double dealAmount) {
        this.dealAmount = dealAmount;
    }

    public double getGetProfit() {
        return getProfit;
    }

    public void setGetProfit(double getProfit) {
        this.getProfit = getProfit;
    }
}
